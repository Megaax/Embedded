#include "Platform_Types.h"
#include "DIO_INTERFACE.h"

#include "DC_interface.h"
#include "DC_cfg.h"
#include "DC_private.h"


/**********************************************************************************************************************
 *  FUNCTIONS IMPLEMENTATION
 *********************************************************************************************************************/

void DC_voidInit ()
{
    /* Configure All pins as Output */
    Dio_WriteChannel ( DC_PORT   , DC_PIN  , Dio_LevelLow);
	
}

void DC_voidGoForward ()
{
    Dio_WriteChannel ( DC_PORT   , DC_PIN  , Dio_LevelHigh);

}

void DC_voidStop ()
{
    Dio_WriteChannel ( DC_PORT   , DC_PIN  , Dio_LevelLow);

}


